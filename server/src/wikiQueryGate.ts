/**
 * Эвристика: запросы не в стиле энциклопедии (личные советы, деньги, быт),
 * для которых поиск по Википедии неуместен.
 */
const OUT_OF_SCOPE_REGEXES: RegExp[] = [
  /как\s+(мне\s+)?заработать\b/i,
  /как\s+заработать\s+деньги/i,
  /где\s+заработать/i,
  /способы\s+заработк/i,
  /как\s+(быстро\s+)?разбогатеть/i,
  /пассивн(ый|ого|ая)\s+доход/i,
  /как\s+похудеть\b/i,
  /как\s+найти\s+работу/i,
  /как\s+уволиться/i,
  /как\s+познакомиться\s+с\b/i,
  /что\s+делать\s+если\s+(он|она|муж|жена)\s/i,
  /порекомендуй(те)?\b/i,
  /что\s+посмотреть\s+(вечером|сегодня)/i,
  /что\s+купить\s+(из|на)\b/i,
  /где\s+купить\s+дешевле/i,
  /напиши(те)?\s+(мне\s+)?(стих|рассказ|сочинение|эссе)\b/i,
  /реши(те)?\s+.*\s+за\s+меня/i,
  /сочинение\s+на\s+тему/i,
  /как\s+заставить\s+(любить|простить)/i,
];

export function isOutOfScopeForWikipedia(query: string): boolean {
  const t = query.trim();
  if (!t) return false;
  const lowered = t.toLowerCase();
  return OUT_OF_SCOPE_REGEXES.some((re) => re.test(lowered));
}
